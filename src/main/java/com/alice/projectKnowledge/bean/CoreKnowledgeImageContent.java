package com.alice.projectKnowledge.bean;

public class CoreKnowledgeImageContent {

	private Integer imageContentId;
	private String imageTitle;
	private String imageContent;
	private String imagePath;
	private String imageFile;
	private Integer imageSort;
	private Integer partId;
	public Integer getImageContentId() {
		return imageContentId;
	}
	public void setImageContentId(Integer imageContentId) {
		this.imageContentId = imageContentId;
	}
	public String getImageTitle() {
		return imageTitle;
	}
	public void setImageTitle(String imageTitle) {
		this.imageTitle = imageTitle;
	}
	public String getImageContent() {
		return imageContent;
	}
	public void setImageContent(String imageContent) {
		this.imageContent = imageContent;
	}
	public String getImagePath() {
		return imagePath;
	}
	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}
	public String getImageFile() {
		return imageFile;
	}
	public void setImageFile(String imageFile) {
		this.imageFile = imageFile;
	}
	public Integer getImageSort() {
		return imageSort;
	}
	public void setImageSort(Integer imageSort) {
		this.imageSort = imageSort;
	}
	public Integer getPartId() {
		return partId;
	}
	public void setPartId(Integer partId) {
		this.partId = partId;
	}
}
