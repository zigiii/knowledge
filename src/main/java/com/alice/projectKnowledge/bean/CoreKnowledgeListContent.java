package com.alice.projectKnowledge.bean;

public class CoreKnowledgeListContent {

	private Integer listContentId;
	private String listContentName;
	private String listContent;
	private Integer listContentSort;
	private Integer partId;
	public Integer getListContentId() {
		return listContentId;
	}
	public void setListContentId(Integer listContentId) {
		this.listContentId = listContentId;
	}
	public String getListContentName() {
		return listContentName;
	}
	public void setListContentName(String listContentName) {
		this.listContentName = listContentName;
	}
	public String getListContent() {
		return listContent;
	}
	public void setListContent(String listContent) {
		this.listContent = listContent;
	}
	public Integer getListContentSort() {
		return listContentSort;
	}
	public void setListContentSort(Integer listContentSort) {
		this.listContentSort = listContentSort;
	}
	public Integer getPartId() {
		return partId;
	}
	public void setPartId(Integer partId) {
		this.partId = partId;
	}
}
