package com.alice.projectKnowledge.bean;

public class CoreSample {

	private Integer sampleId;
	private String sampleTitle;
	private String sampleContent;
	private String sampleUnscramble;
	private Integer relationId;
	public Integer getSampleId() {
		return sampleId;
	}
	public void setSampleId(Integer sampleId) {
		this.sampleId = sampleId;
	}
	public String getSampleTitle() {
		return sampleTitle;
	}
	public void setSampleTitle(String sampleTitle) {
		this.sampleTitle = sampleTitle;
	}
	public String getSampleContent() {
		return sampleContent;
	}
	public void setSampleContent(String sampleContent) {
		this.sampleContent = sampleContent;
	}
	public String getSampleUnscramble() {
		return sampleUnscramble;
	}
	public void setSampleUnscramble(String sampleUnscramble) {
		this.sampleUnscramble = sampleUnscramble;
	}
	public Integer getRelationId() {
		return relationId;
	}
	public void setRelationId(Integer relationId) {
		this.relationId = relationId;
	}
}
