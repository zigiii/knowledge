package com.alice.projectKnowledge.bean;

public class CoreCharacteristic {

	private Integer characteristicId;
	private String characteristicName;
	private String characteristicDefinition;
	private Integer pointId;
	private String existSample;
	public Integer getCharacteristicId() {
		return characteristicId;
	}
	public void setCharacteristicId(Integer characteristicId) {
		this.characteristicId = characteristicId;
	}
	public String getCharacteristicName() {
		return characteristicName;
	}
	public void setCharacteristicName(String characteristicName) {
		this.characteristicName = characteristicName;
	}
	public String getCharacteristicDefinition() {
		return characteristicDefinition;
	}
	public void setCharacteristicDefinition(String characteristicDefinition) {
		this.characteristicDefinition = characteristicDefinition;
	}
	public Integer getPointId() {
		return pointId;
	}
	public void setPointId(Integer pointId) {
		this.pointId = pointId;
	}
	public String getExistSample() {
		return existSample;
	}
	public void setExistSample(String existSample) {
		this.existSample = existSample;
	}
	
}
