package com.alice.projectKnowledge.bean;

public class CoreKnowledgePointPart {

	private Integer partId;
	private String partName;
	private String partShowType;
	private Integer pointId;
	public Integer getPartId() {
		return partId;
	}
	public void setPartId(Integer partId) {
		this.partId = partId;
	}
	public String getPartName() {
		return partName;
	}
	public void setPartName(String partName) {
		this.partName = partName;
	}
	public String getPartShowType() {
		return partShowType;
	}
	public void setPartShowType(String partShowType) {
		this.partShowType = partShowType;
	}
	public Integer getPointId() {
		return pointId;
	}
	public void setPointId(Integer pointId) {
		this.pointId = pointId;
	}
}
