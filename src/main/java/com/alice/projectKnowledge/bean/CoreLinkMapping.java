package com.alice.projectKnowledge.bean;

public class CoreLinkMapping {

	private Integer linkMappingId;
	private String linkKnowledgePoint;
	private String linkKnowledgeCode;
	
	public Integer getLinkMappingId() {
		return linkMappingId;
	}
	public void setLinkMappingId(Integer linkMappingId) {
		this.linkMappingId = linkMappingId;
	}
	public String getLinkKnowledgePoint() {
		return linkKnowledgePoint;
	}
	public void setLinkKnowledgePoint(String linkKnowledgePoint) {
		this.linkKnowledgePoint = linkKnowledgePoint;
	}
	public String getLinkKnowledgeCode() {
		return linkKnowledgeCode;
	}
	public void setLinkKnowledgeCode(String linkKnowledgeCode) {
		this.linkKnowledgeCode = linkKnowledgeCode;
	}
	
}
